using Content.Client.Chat;
using Content.Client.Chat.Controllers;
using Content.Client.Chat.Controls;
using Content.Shared.Chat;
using Robust.Client.AutoGenerated;
using Robust.Client.Player;
using Robust.Client.UserInterface;
using Robust.Client.UserInterface.Controls;
using Robust.Client.UserInterface.XAML;
using Robust.Shared.Utility;

namespace Content.Client.Lobby;

[GenerateTypedNameReferences]
public sealed partial class LobbyUI : UIScreen, IChatHandler
{
    [Dependency] private readonly IEntitySystemManager _entitySystemManager = default!;
    [Dependency] private readonly IEntityManager _entityManager = default!;
    [Dependency] private readonly IPlayerManager _playerManager = default!;
    
    public LobbyUI()
    {
        RobustXamlLoader.Load(this);
        IoCManager.InjectDependencies(this);
        LayoutContainer.SetAnchorPreset(RootContainer, LayoutPreset.Wide);
        
        SendButton.OnPressed += SendButtonOnOnPressed;
    }

    private void SendButtonOnOnPressed(BaseButton.ButtonEventArgs obj)
    {
        _entitySystemManager.GetEntitySystem<ChatSystem>().Send(TextField.Text);
        TextField.Clear();
    }

    public void AddMessage(UserComponent component, string message)
    {
        var entry = new ChatEntry();
        entry.SetUser(component);
        entry.SetMessage(message);
        ChatBox.Children.Add(entry);
    }

    public void Clear()
    {
        ChatBox.Children.Clear();
    }

    public void SetLocalUsername(string name)
    {
        CurrentUserLabel.Text = name;
    }
}